#include <unistd.h>

int	ft_atoi(char *str)
{
	int	n;
	int	S;
	int	i;

	i = 0;
	n = 1;
	S = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			n = n * -1;
		i++;
	}
	while (str[i] != '\0' && (str[i] <= '9' && str[i] >= '0'))
	{
		S = S * 10;
		S = S + (str[i] - '0');
		i++;
	}
	return (S * n);
}
